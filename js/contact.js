// javascript to connect the box 6 to enter name
document.addEventListener('DOMContentLoaded', function() {
    var box6 = document.getElementById('box6');
    var input = document.createElement('input');
    input.type = 'text';
    input.placeholder = '...';

    // Append the input field inside the grid item
    document.getElementById('box6').appendChild(input);
    box6.appendChild(input);
});

